import numpy as np

a = np.array([2,4,9,10],float)
print(np.sqrt(a),"\n------")#Raiz cuadrada de cada elememto de un array
print(np.exp(a),"\n------")# Elevar el numero de euler al exponente tomandolo del array(e^x)
print(np.log(a),"\n------")#Le saca el logaritmo neperiano a cada elemento(ln(x))
print(np.sin(a),"\n------")#Saca el seno
print(np.mean(a),"\n------")#Media aritmetica del array
